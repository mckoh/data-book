CREATE DATABASE starwars;
USE starwars;

CREATE TABLE starwars.entity (
    entity_id INT,
    shortname VARCHAR(25),
    longname VARCHAR(40),
    number_of_scenes INT,
    PRIMARY KEY (entity_id)
);

CREATE TABLE starwars.episode (
	episode_id INT,
    shortname VARCHAR(50),
    longname VARCHAR(150),
    PRIMARY KEY (episode_id)
);

CREATE TABLE starwars.interaction (
	interaction_id INT auto_increment,
    `from` INT,
    `to` INT,
    number_of_scenes INT,
    episode_id INT,
    PRIMARY KEY (interaction_id)
);

